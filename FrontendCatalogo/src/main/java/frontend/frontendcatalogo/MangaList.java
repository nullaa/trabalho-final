    public void loadTableData() {
        RestTemplate req = new RestTemplate();
        
        ResponseEntity<Manga[]> response = req.getForEntity(
                "http://localhost:8080/mangas", Manga[].class);
        
        Manga[] mangas = response.getBody();
        
        DefaultTableModel tbl = (DefaultTableModel) tblMangas.getModel();
        
        tbl.setNumRows(0);
        
        for (var c : mangas) {
            Object[] row = { c.getId(), c.getAutor(), c.getTitulo(), c.getGenero() };
            tbl.addRow(row);
        }
    }
