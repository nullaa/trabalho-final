    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        
        Manga manga = new Manga();
        
        manga.setAutor((Autor) auAutor.getSelectedItem());
        manga.setTitulo(tTitulo.getText());
        manga.setGenero(gGenero.getText());
     
        try {
            RestTemplate req = new RestTemplate();
            
            req.postForObject("http://localhost:8080/manga", 
                    manga, Manga.class);
            JOptionPane.showMessageDialog(this, "Mangá  cadastrado com sucesso!");
        } catch (RestClientException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
        MangaList m =new MangaList();
        m.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btCadastrarActionPerformed

