    public void loadAutores() {
        RestTemplate req = new RestTemplate();
        
        ResponseEntity<Autor[]> response = req.getForEntity(
                "http://localhost:8080/autores", Autor[].class);
        
        Autor[] autores = response.getBody();
        
        for (var c : autores) {
          auAutor.addItem(c);
        }
    }

